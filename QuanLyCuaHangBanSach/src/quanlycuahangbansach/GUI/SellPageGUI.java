    private void btnTimSachWithIDOrSerialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimSachWithIDOrSerialActionPerformed
        // TODO add your handling code here:
        
        
        String values=edtMaSachOrSerialSachTK.getText();
        danhSachSanPham = ProductDAO.getListProductByValues(values);
        if(danhSachSanPham.size()==1)
        {
            panelSp1.setVisible(true);
            panelSp2.setVisible(false);
            panelSp3.setVisible(false);
            btnNextSp.setVisible(false);
            LoadPanelSanPham1(danhSachSanPham.get(0));
            
        }
        else if(danhSachSanPham.size()==2)
        {
            panelSp1.setVisible(true);
            panelSp2.setVisible(true);
            panelSp3.setVisible(false);
            btnNextSp.setVisible(false);
            LoadPanelSanPham1(danhSachSanPham.get(0));
            LoadPanelSanPham2(danhSachSanPham.get(1));
        
        }
        else if(danhSachSanPham.size()==3)
        {
            panelSp1.setVisible(true);
            panelSp2.setVisible(true);
            panelSp3.setVisible(true);
            btnNextSp.setVisible(false);
            LoadPanelSanPham1(danhSachSanPham.get(0));
            LoadPanelSanPham2(danhSachSanPham.get(1));
            LoadPanelSanPham3(danhSachSanPham.get(2));
        }
        else
        {
            panelSp1.setVisible(true);
            panelSp2.setVisible(true);
            panelSp3.setVisible(true);
            btnNextSp.setVisible(true);
            Product sp1=new Product(danhSachSanPham.get(0));
            Product sp2=new Product(danhSachSanPham.get(1));
            Product sp3=new Product(danhSachSanPham.get(2));
            LoadPanelSanPham1(sp1);
            LoadPanelSanPham2(sp2);
            LoadPanelSanPham3(sp3);
            danhSachSanPham.remove(sp1);
            danhSachSanPham.remove(sp2);
            danhSachSanPham.remove(sp3);
        }
    }//GEN-LAST:event_btnTimSachWithIDOrSerialActionPerformed

    
    
    private void btnTaoHoaDonMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoHoaDonMoiActionPerformed
        // TODO add your handling code here:
        edtMaNhanVien.setText(NhavVienDangDung.getMaNhanVien());
        String soDienThoai=edtSDTKhachHang_PBH.getText();
        SalesSlip s=new SalesSlip();
        s.setMaNhanVien(NhavVienDangDung.getMaNhanVien());
        s.setSoDienThoaiKhachHang(soDienThoai);
        int kq=SalesSlipDAO.insertOneSalesSlip(s);
        if(kq>0)
        {
            JOptionPane.showMessageDialog(this, "Đã thêm mới hóa đơn.");
            float tongTien=Float.valueOf(edtTongTien.getText());
            //tinh tien giam gia
            Customer customer=new Customer();
            customer=CustomerDAO.getOneByID(soDienThoai);
            float giamGia_=tongTien*customer.getGiamGia();
            float giamGia=0;
            if(giamGia_%1000<500)
            {
                giamGia=(float) Math.ceil(giamGia_/1000)*1000;
            }
            else if(giamGia_%1000<999)
            {
                giamGia=(float) Math.floor(giamGia_/1000)*1000;
            }
            
            edtSoTienGiamGia.setText(String.valueOf(giamGia));
            //goi ma hoa don
            SalesSlip sAfter=SalesSlipDAO.getLatestSalesSlipByIDSalePerson(NhavVienDangDung.getMaNhanVien());
            edtMaHoaDon.setText(sAfter.getMaPhieuBanHang());
            
            float tongTienPhaiTra=(float)tongTien-giamGia;
            edtTongTienPhaiTra.setText(String.valueOf(tongTienPhaiTra));
            
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Lỗi.");
        }
        
    }//GEN-LAST:event_btnTaoHoaDonMoiActionPerformed

    
