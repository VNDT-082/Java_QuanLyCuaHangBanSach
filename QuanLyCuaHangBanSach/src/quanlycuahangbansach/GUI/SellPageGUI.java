    private void btnThemSanPhamHoaDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemSanPhamHoaDonActionPerformed
        // TODO add your handling code here:
        String maSanPham=edtMaSanPhamCTHD.getText();
        int dem=0;
        Vector<String> v=new Vector<String>();
        v.add(edtMaSanPhamCTHD.getText());
        v.add(edtTenSanPhamCTHD.getText());
        int soLuong =Integer.valueOf(edtSoLuongSanPhamCTHD.getText());
        double giaBan=Double.valueOf(edtGiaBanSanPhamCTHD.getText());
        double tongTien= soLuong*giaBan;
        v.add(edtSoLuongSanPhamCTHD.getText());
        v.add(edtGiaBanSanPhamCTHD.getText());
        v.add(String.valueOf(tongTien));
        DefaultTableModel dtm=(DefaultTableModel)tableChiTietSanPham.getModel();
        for(int i=0;i<dtm.getRowCount();i++)
        {
            String temp=dtm.getValueAt(i, 0).toString();
            if(temp.equals(maSanPham))
            {
                dem++;
            }
        }
        if(dem==0)
        {
            DanhSachChiTietPhieuBanHang.add(v);
            
            tableChiTietSanPham.updateUI();
            float tongTien_ =0;
            DefaultTableModel tbm= (DefaultTableModel)tableChiTietSanPham.getModel();
            for(int i=0;i<tbm.getRowCount();i++)
            {
                tongTien_+=Float.valueOf(tbm.getValueAt(i, 4).toString());
            }
            edtTongTien.setText(String.valueOf(tongTien_));
        }
        else
        {
            int n=JOptionPane.showConfirmDialog(this, "Sản phẩm đã có trong giỏ hàng.\n"
                    + "Bạn có muốn thay đổi số lượng mua?","Thông báo",JOptionPane.YES_NO_OPTION);
            if(n==JOptionPane.YES_OPTION)
            {
                for(int i=0; i<dtm.getRowCount();i++)
                {
                    if(dtm.getValueAt(i, 0).equals(maSanPham))
                    {
                        soLuong =Integer.valueOf(edtSoLuongSanPhamCTHD.getText());
                        giaBan=Double.valueOf(edtGiaBanSanPhamCTHD.getText());
                        tongTien= soLuong*giaBan;
                        tableChiTietSanPham.setValueAt(soLuong, i, 2);
                        tableChiTietSanPham.setValueAt(tongTien, i, 4);
                        float tongTien_ =0;
                        DefaultTableModel tbm= (DefaultTableModel)tableChiTietSanPham.getModel();
                        for(int j=0;j<tbm.getRowCount();j++)
                        {
                            tongTien_+=Float.valueOf(tbm.getValueAt(j, 4).toString());
                        }
                        edtTongTien.setText(String.valueOf(tongTien_));
                    }
                }
                
            }
        }
           
    }//GEN-LAST:event_btnThemSanPhamHoaDonActionPerformed

