    private void btnXacNhanDonHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanDonHangActionPerformed
        // TODO add your handling code here:
        double soTienDaNhan=0;
        double tongTien=Double.valueOf(edtTongTienPhaiTra.getText());
        soTienDaNhan=Double.valueOf(edtSoTienDaNhan.getText());
        float soTienPhaiTra=Float.valueOf(edtTongTienPhaiTra.getText());
        if(soTienDaNhan<soTienPhaiTra)
        {
            JOptionPane.showMessageDialog(this, "Khách hàng trả chưa đủ tiền.\n"
                    + "Vui lòng kiểm tra lại!");
        }
        else
        {
            double tienThua=soTienDaNhan-tongTien;
            edtSoTienThua.setText(String.valueOf(tienThua));
            int kq=0;
            DefaultTableModel dtm=(DefaultTableModel) tableChiTietSanPham.getModel();
            for(int i=0;i<dtm.getRowCount();i++)
            {
                InvoiceDetails invoice=new InvoiceDetails();
                invoice.setMaPhieuBan(edtMaHoaDon.getText());
                invoice.setMaSach(dtm.getValueAt(i, 0).toString());
                invoice.setSoLuong(Integer.valueOf(dtm.getValueAt(i, 2).toString()));
                invoice.setTongTien(Float.valueOf(dtm.getValueAt(i, 3).toString()));
                kq=InvoiceDetailsDAO.insertOneInvoiceDetails(invoice);
                if(kq==0)
                {
                    JOptionPane.showMessageDialog(this, "Lỗi: "+invoice.getMaSach()+"không thể thêm vào hóa đơn.");
                }
                
            }
            SalesSlip s=new SalesSlip();
            s.setMaNhanVien(NhavVienDangDung.getMaNhanVien());
            s.setSoDienThoaiKhachHang(edtSDTKhachHang_PBH.getText());
            s.setMaPhieuBanHang(edtMaHoaDon.getText());
            s.setGiamGia(Float.valueOf(edtGiamGia_KH.getText()));
            s.setSoTienPhiaTra(Float.valueOf(edtTongTienPhaiTra.getText()));
            
           // s.setSoTienPhiaTra(edtTon);
            kq=SalesSlipDAO.updateOneSalesSlip(s);
            if(kq>0)
            {
                JOptionPane.showMessageDialog(this, "Thanh toán thành công.");
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Lỗi.");
            }
            
        }
    }//GEN-LAST:event_btnXacNhanDonHangActionPerformed

    private void btnTimKiemHoaDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemHoaDonActionPerformed
        // TODO add your handling code here:
        int index=cbbTimKiemTheo.getSelectedIndex();
        if(index==0)
        {
            cbbKetQuaTimHoaDon.removeAllItems();
            String ngay=edtThongTinTimKiem.getText();
            ArrayList<SalesSlip> ds=SalesSlipDAO.getListSalesSlipByDateValue(ngay);
            for(SalesSlip s:ds)
            {
                cbbKetQuaTimHoaDon.addItem(s);
            }
            
        }
        else if(index==1)
        {
            String maHoaDon=edtThongTinTimKiem.getText();
            SalesSlip s=SalesSlipDAO.getOneSalesSlipByIDSalesSlip(maHoaDon);
            if(s!=null)
            {
                edtSDT_KH.setText(s.getSoDienThoaiKhachHang());
                edtMaHoaDon.setText(s.getMaPhieuBanHang());
                edtSDTKhachHang_PBH.setText(s.getSoDienThoaiKhachHang());
                edtTongTien.setText(String.valueOf(s.getTongTien()));
                edtTongTienPhaiTra.setText(String.valueOf(s.getSoTienPhiaTra()));
                float giamGia=s.getTongTien()-s.getSoTienPhiaTra();
                edtSoTienGiamGia.setText(String.valueOf(giamGia));
                
            }
        }
        else if(index==2)
        {
            String sdt=edtThongTinTimKiem.getText();
            ArrayList<SalesSlip> ds=SalesSlipDAO.getListSalesSlipByPhoneNumberValue(sdt);
            cbbKetQuaTimHoaDon.removeAllItems();
            for(SalesSlip s:ds)
            {
                cbbKetQuaTimHoaDon.addItem(s);
            }
        }
    }//GEN-LAST:event_btnTimKiemHoaDonActionPerformed

